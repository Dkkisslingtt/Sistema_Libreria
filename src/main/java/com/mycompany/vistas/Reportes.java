    private void cargarReportes() {
        try {
            DAOPrestamos dao = new DAOPrestamoImpl();
            DefaultTableModel model = (DefaultTableModel) tableReportes.getModel();
            // Limpiamos tabla
            model.setRowCount(0);
            dao.listar().forEach((u) -> model.addRow(new Object[]{u.getUsuario_id(), u.getLibro_id(), u.getFecha_salida(), u.getFecha_devuelto()}));
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
